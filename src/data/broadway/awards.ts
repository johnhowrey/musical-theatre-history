export function getTonyWinner(year: number): string | undefined {
  const award = TONY_AWARDS.find(a => a.year === year);
  if (!award) return undefined;
  const bestMusical = award.categories.find(c => c.category === 'Best Musical');
  return bestMusical?.winner;
}

export function getTonyYear(showTitle: string): TonyAwardYear | undefined {
  return TONY_AWARDS.find(a =>
    a.categories.some(c => c.winner === showTitle || c.nominees?.includes(showTitle))
  );
}
